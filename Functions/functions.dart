void main() {
  //This is main function. It will run first when we execute file.
  print(fucWithStr());
  print(fucWithBool());
  print(fucWithInt());
  print(fucWithVar());
}

String fucWithStr() {
  //This is a string function. It will run our code and return a string.
  return "HELLO WORLD!";
}

bool fucWithBool() {
  //This is a bool function. It will run our code and return a bool.
  return false;
}

int fucWithInt() {
  //This is a int function. It will run our code and return a int.
  return 200;
}

num fucWithVar() {
  //This is a num function. It will run our code and return a num.
  return 300.001;
}
